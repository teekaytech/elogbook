1. Write a simple program to calculate the date of birth of a user, 
accepting the only age from user as input. 
Hint: Use system's current date for calculation.

2. Write a program that reads the subtotal and the gratuity rate, 
then computes the gratuity and total. For example, if the user
enters 10 for subtotal and 15% for gratuity rate, 
the program displays $1.5 as gratuity and $11.5 as total.

3. Write a program that reads an integer between
0 and 1000 and adds all the digits in the integer. For example, if an integer is 932,
the sum of all its digits is 14.
Hint: Use the % operator to extract digits, and use the / operator to remove the
extracted digit. For instance, 932 % 10 = 2 and 932 / 10 = 93.